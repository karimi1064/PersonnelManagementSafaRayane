package com.example.personnelmanagement.Models;


public class DataModel {
    int id;
    String name;
    String family;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        name = name;
    }

    public String getFamily() {
        return family;
    }

    public void setFamily(String family) {
        family = family;
    }
}
